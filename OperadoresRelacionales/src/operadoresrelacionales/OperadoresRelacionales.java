package operadoresrelacionales;


public class OperadoresRelacionales {

    
    public static void main(String[] args) {
      
        int a=8,b=6;
        System.out.println("a==b "+(a==b));
        System.out.println("a!=b "+(a!=b));
        System.out.println("a>b "+(a>b));
        System.out.println("a>=b "+(a>=b));
        System.out.println("a<b "+(a<b));
        System.out.println("a<=b "+(a<=b));
        
    }
    
}
